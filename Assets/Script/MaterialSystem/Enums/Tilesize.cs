/// <summary>
/// Predefined tile sizes for ceramic/tile materials.
/// Values represent tile dimensions in centimeters.
/// </summary>
public enum TileSize
{
    /// <summary>
    /// Small tile size: 30cm x 30cm (common for bathrooms, small rooms)
    /// </summary>
    Small_30cm = 30,

    /// <summary>
    /// Medium tile size: 60cm x 60cm (DEFAULT - most common for living areas)
    /// </summary>
    Medium_60cm = 60,

    /// <summary>
    /// Large tile size: 90cm x 90cm (modern, spacious look)
    /// </summary>
    Large_90cm = 90
}